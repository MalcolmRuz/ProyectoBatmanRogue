    private void Jbtn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbtn_guardarActionPerformed
        //guardar los datos del nuevo usuario ingresado
        String nombre = this.jtxt_Nick.getText();
        int edad;
        try {
            edad = Integer.parseInt(this.jtxt_edad.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"La debe debe ser un numero valido!","ERROR",JOptionPane.ERROR_MESSAGE);
            return; //detenerse si la edad es invalida
        }
        
        String genero = "";
        
        if (this.jrbtn_hombre.isSelected()) {
            genero = "Masculino";
        }else if (this.jrbtn_mujer.isSelected()) {
            genero = "Femenino";
        }else if (this.jrbtn_otro.isSelected()) {
            genero = "Otro";
        }
        
        //si el genero no quedo marcado e intentan guardar
        if (genero.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Debe seleccionar un g√©nero","ERROR",JOptionPane.ERROR_MESSAGE);
            return; //detiene la ejecucion para que no se guarde con genero vacio
        }

        int puntaje = 0;
        
        Usuario usuario = new Usuario(nombre, genero, edad, puntaje);
        Encuentro encuentro = new Encuentro();
        if (encuentro.crearUsuario(usuario)) {
            Usuario usuarioCompleto = encuentro.buscarPorNombreUsuario(nombre); // recuperar la instancia completa de la bd
            if (usuarioCompleto != null) {
                JOptionPane.showMessageDialog(null,"Usuario Ingresado!","Ingresar Usuario", 1);
                Form_SeleccionarHeroe sh = new Form_SeleccionarHeroe(usuarioCompleto);
                sh.setVisible(true);
                this.dispose();
            }else{ // caso si es que se guardo pero no se encontro al buscar
                JOptionPane.showMessageDialog(null,"Usuario ingresado, pero no se pudo cargar","Error de carga", 0);
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Usuario no Ingresado","Ingresar Usuario",0);
        }
    }//GEN-LAST:event_Jbtn_guardarActionPerformed

