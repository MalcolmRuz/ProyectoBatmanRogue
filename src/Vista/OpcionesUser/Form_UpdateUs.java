    private void jbtn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_searchActionPerformed
        // Buscar al usuario y rellene lo demas
        String nombre = this.jtxt_buscarNombre.getText().trim();
        Encuentro encuentro = new Encuentro();

        Usuario usu = encuentro.buscarPorNombreUsuario(nombre);

        if (usu != null) { // si se encuentra el usuario

            this.Jtxt_nombreActualizar.setText(usu.getNombreUsuario()); //rellenando los campos de jtxt 
            this.Jtxt_edadActualizar.setText(String.valueOf(usu.getEdad()));
            
            String genero = usu.getGenero();
            
            //ahora se selecciona el acorde al usuario
            if ("Masculino".equalsIgnoreCase(genero)|| "Male".equalsIgnoreCase(genero)) { //vverifica si en la bd esta guardado como Masculino o Male(OpenEnglish nunca se sabe)
                this.jrbtn_hombreActualizar.setSelected(true);
            }else if("Femenino".equalsIgnoreCase(genero)||"Female".equalsIgnoreCase(genero)){
                this.jrbtn_mujerActualizar.setSelected(true);
            }else if ("Otro".equalsIgnoreCase(genero)||"Other".equalsIgnoreCase(genero)) {
                this.jrbtn_otroActualizar.setSelected(true);
            }

            
        }else{
            JOptionPane.showMessageDialog(null,"Usuario no encontrado", "Busqueda Usuario",0);
            this.Jtxt_nombreActualizar.setText("");
            this.Jtxt_edadActualizar.setText("");
            this.jbg_genero.clearSelection();
            this.jtxt_buscarNombre.setText("");
        }


    }//GEN-LAST:event_jbtn_searchActionPerformed

