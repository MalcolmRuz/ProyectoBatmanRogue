    private void Jbtn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbtn_ActualizarActionPerformed
        // Actualizar los datos del Usuario (nombre,genero,edad)
        String nombreActualizado = this.Jtxt_nombreActualizar.getText().trim();
        String nombreOriginal = this.jtxt_buscarNombre.getText().trim();
        
        //comprobar que la edad sea valida
        int edad;
        try {
            edad = Integer.parseInt(this.Jtxt_edadActualizar.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"La edad debe ser un numero entero valido","ERROR",0);
            return;//detiene la ejecucion si hay error
        }
        
        //obtener genero de los jrbtn
        String genero = "";
        if (this.jrbtn_hombreActualizar.isSelected()) {
            genero = "Masculino";
        }else if (this.jrbtn_mujerActualizar.isSelected()) {
            genero = "Femenino";
        }else if (this.jrbtn_otroActualizar.isSelected()) {
            genero = "Otro";
        }
        
        if (genero.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Debe seleccionar un genero","ERROR",0);
            return;
        }
        
        
        Encuentro encuentro = new Encuentro();
        
        int puntaje = encuentro.obtenerPuntaje(nombreOriginal);
        if (puntaje == -1) {
            JOptionPane.showMessageDialog(null,"No se encontro puntaje del usuario original", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        
        
        Usuario usuarioActualizado = new Usuario(nombreActualizado, genero, edad, puntaje);
        
        
        if (encuentro.actualizarUsuario(usuarioActualizado, nombreOriginal)) {
            JOptionPane.showMessageDialog(null,"Usuario " + nombreActualizado + " actualizado con exito!","Actualizacion exitosa!",1);
            
            this.jtxt_buscarNombre.setText("");
            this.Jtxt_nombreActualizar.setText("");
            this.Jtxt_edadActualizar.setText("");
            this.jbg_genero.clearSelection();
            this.jtxt_buscarNombre.requestFocus();
        }else{
            JOptionPane.showMessageDialog(null,"Error al actualizar el usuario, revisa bd o si el usuario existe", "ERROR",0);
        }
        
    }//GEN-LAST:event_Jbtn_ActualizarActionPerformed

