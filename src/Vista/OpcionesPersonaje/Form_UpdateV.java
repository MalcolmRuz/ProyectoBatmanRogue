    private void Jbtn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbtn_ActualizarActionPerformed

        String nombreActualizado = this.jtxt_nombreVillano.getText().trim();
        String nombreOriginal = this.jtxt_buscarVillano.getText().trim(); // El nombre que usamos para buscar
        String ataqueEspecial = this.jtxt_ataqueEspecial.getText().trim();

        // Determinar Nivel de Dificultad
        int nivelDificultad = 0;
        if (this.jrbtn_nivelUno.isSelected()) {
            nivelDificultad = 1;
        } else if (this.jrbtn_nivelDos.isSelected()) {
            nivelDificultad = 2;
        } else if (this.jrbtn_nivelTres.isSelected()) {
            nivelDificultad = 3;
        }

        // determinar si es Jefe
        boolean esJefe = this.jrbtn_boss.isSelected();

        // validacion 
        if (nombreActualizado.isEmpty() || ataqueEspecial.isEmpty()) {
            JOptionPane.showMessageDialog(null, "El nombre y el Ataque Especial no pueden estar vacíos.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (nivelDificultad == 0) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un Nivel de Dificultad.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // crear objeto Villano con los datos actualizados
        Villano villanoActualizado = new Villano();
        villanoActualizado.setNombre(nombreActualizado);
        villanoActualizado.setTextoATQespecial(ataqueEspecial);
        villanoActualizado.setIndiceDificultad(nivelDificultad);
        villanoActualizado.setEsJefe(esJefe);

        
        Encuentro encuentro = new Encuentro();

        // llamar al método de actualización (requiere el nombre original como referencia)
        if (encuentro.actualizarVillano(villanoActualizado, nombreOriginal)) {
            JOptionPane.showMessageDialog(null, "Villano " + nombreActualizado + " actualizado con éxito!", "Actualización exitosa!", JOptionPane.INFORMATION_MESSAGE);

            // limpiar campos después del éxito
            Jbtn_LimpiarActionPerformed(evt);

        } else {
            JOptionPane.showMessageDialog(null, "Error al actualizar el villano. Revisa la BD o si el nombre original existe.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_Jbtn_ActualizarActionPerformed

