    private void jbtn_buscarPorNombreVillanoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_buscarPorNombreVillanoActionPerformed
        // obtener el nombre a buscar
        String nombre = this.jtxt_buscarVillano.getText().trim();

        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el nombre del villano a buscar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            this.jtxt_buscarVillano.requestFocus();
            return;
        }

        Encuentro encuentro = new Encuentro();

        // buscar al Villano (asumiendo que los campos de actualización usan los mismos nombres que los de creación: jtxt_nombreVillano, jtxt_ataqueEspecial)
        Villano villano = encuentro.buscarPorNombreVillano(nombre);

        if (villano != null) {
            // rellenar los campos de actualización
            this.jtxt_nombreVillano.setText(villano.getNombre());
            this.jtxt_ataqueEspecial.setText(villano.getTextoATQespecial());

            // seleccionar Nivel de Dificultad (usando los jrbtn de nivel)
            int nivel = villano.getIndiceDificultad();
            switch (nivel) {
                case 1:
                    this.jrbtn_nivelUno.setSelected(true);
                    break;
                case 2:
                    this.jrbtn_nivelDos.setSelected(true);
                    break;
                case 3:
                    this.jrbtn_nivelTres.setSelected(true);
                    break;
                default:
                    this.jbg_nivel.clearSelection();
                    break;
            }

            // seleccionar Tipo (esJefe) (usando los jrbtn de tipo)
            boolean esJefe = villano.isEsJefe();
            if (esJefe) {
                this.jrbtn_boss.setSelected(true);
            } else {
                this.jrbtn_villano.setSelected(true);
            }

        } else {
            // mensaje de error y limpieza
            JOptionPane.showMessageDialog(null, "Villano no encontrado.", "Busqueda Villano", JOptionPane.INFORMATION_MESSAGE);

            this.jtxt_nombreVillano.setText("");
            this.jtxt_ataqueEspecial.setText("");
            this.jbg_nivel.clearSelection();
            this.jbg_tipo.clearSelection();
            this.jtxt_buscarVillano.requestFocus();
        }
    }//GEN-LAST:event_jbtn_buscarPorNombreVillanoActionPerformed

